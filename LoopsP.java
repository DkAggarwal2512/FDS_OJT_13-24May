// Do--while

public class LoopsP{
    public static void main(String[] args) {
        int i = 12;
        while(i<11){
            System.out.println("IBM");
        }
        
        do{
            System.out.println("IBM");
        }while(i<11);
        }
        
        
}

// 2. For Loop

public class For{
    public static void main(String arg[]){
        // (Initialisation; Condation; Updation)
        // for (int a = 0; a <3; a = a + 1)
        for(int a =0; a< 11; a++)
        {
            System.out.println(a+ " ");
        }
    }
}


// 3. While Loop

public class While{
    public static void main(String[] args) {
        // (Condation)
        int i = 0;
        while (i < 11) {
            System.out.println(i);
            i++;
        }}
}